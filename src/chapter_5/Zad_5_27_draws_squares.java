package chapter_5;

/**
 * draws squares
 */
public class Zad_5_27_draws_squares {
    public static void main(String[] args) {

        for(int i = 1; i <= 5; i++) {
            for(int j = 1; j <= 3; j++) {
                for(int k = 1; k <= 4; k++) {
                    System.out.print('*');
                }

                System.out.println();
            }

            System.out.println();
        }


    }
}
